    public void showRanking() {
        List<Album> albums;
        HashMap<String, String> artistAndTheirQuantities = new HashMap<>();
        int firstPlaceAlbumsQnt = 0;
        int secondPlaceAlbumsQnt = 0;
        int thirdPlaceAlbumsQnt = 0;
        String firstPlaceArtist = "";
        String secondPlaceArtist = "";
        String thirdPlaceArtist = "";
        int qnt;
            
        try {
            albums = Album.searchAlbums();
            for (int i = 0; i < albums.size(); i++) {
                qnt = 0;
                for (int y = 0; y < albums.size(); y++) {
                    if (albums.get(i).getArtistName()
                            .equals(albums.get(y).getArtistName())) {
                        qnt++;
                        artistAndTheirQuantities.
                                put(albums.get(i).getArtistName(), String.valueOf(qnt));
                    }
                }
            }
            
            for (String name : artistAndTheirQuantities.keySet()) {
                qnt = Integer.parseInt(artistAndTheirQuantities.get(name));
                
                if (qnt > firstPlaceAlbumsQnt) {
                    firstPlaceAlbumsQnt = qnt;
                    firstPlaceArtist = name;
                } else if (qnt < firstPlaceAlbumsQnt && (qnt > thirdPlaceAlbumsQnt)
                        && qnt != secondPlaceAlbumsQnt) {
                    secondPlaceAlbumsQnt = qnt;
                    secondPlaceArtist = name;
                } else {
                    thirdPlaceAlbumsQnt = qnt;
                    thirdPlaceArtist = name;
                }
            }
            
            showFirstArtist(firstPlaceArtist, firstPlaceAlbumsQnt);
            showSecondArtist(secondPlaceArtist, secondPlaceAlbumsQnt);
            showThirdArtist(thirdPlaceArtist, thirdPlaceAlbumsQnt);
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }
    }
    
