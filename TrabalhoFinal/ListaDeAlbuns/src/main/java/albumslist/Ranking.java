    public void showInfo(JLabel place, JLabel lblArtist, String artist, int qnt) {
        switch (qnt) {
            case 0:
                place.setVisible(false);
                break;
            case 1:
                lblArtist.setText(artist.toUpperCase() + ", " + qnt + " álbum");
                break;
            default:
                lblArtist.setText(artist.toUpperCase() + ", " + qnt + " álbuns");
                break;
        }
    }
  
