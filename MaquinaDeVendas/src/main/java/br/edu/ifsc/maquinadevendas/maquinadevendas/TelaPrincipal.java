    private void tfCodigoKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tfCodigoKeyReleased
        String codigo = tfCodigo.getText();
        String descricao = "";
        
        if (codigo.length() > 0) {
            Integer codigoProduto = Integer.parseInt(codigo);
            
            for (Produto p : produtos) {
                if (p.getId() == codigoProduto) {
                    descricao = p.getDescr();
                }
            }
            
            lblProdutoSelecionado.setText(descricao);
        }
    }//GEN-LAST:event_tfCodigoKeyReleased

    private void btnComprarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnComprarActionPerformed
        String codProd = tfCodigo.getText();
        Integer cod = -1;
        Object sel;
        Produto prod = null;
        FormaPgto forma = null;
                
        if (codProd.length() > 0) {
            cod = Integer.parseInt(codProd);
        } else {
            JOptionPane.showMessageDialog(this, "Você deve informar um Produto");
            return;
        }
        
        for (Produto p : produtos) {
            if (p.getId() == cod) {
                prod = p;
            }
        }
        
        if (prod == null) {
            JOptionPane.showMessageDialog(this, "Produto inválido!");
            return;
        }
        
        try {
            formasDePgto = FormaPgto.buscaFormasPgto();
            Object[] opcoes = formasDePgto.stream().map(f -> f.getDescr())
                    .collect(Collectors.toList()).toArray();
            sel = JOptionPane.showInputDialog(this,
                    "Selecione a Forma de Pagamento...", "Formas de Pagamento",
                    JOptionPane.QUESTION_MESSAGE, null, 
                    opcoes, opcoes[0]);
        } catch (Exception ex) {
            System.out.println(ex.getMessage());
            return;
        }
        
        for (FormaPgto f : formasDePgto) {
            if (f.getDescr().equals(sel)) {
                forma = f;
            }
        }
        
        if (forma != null) {
            Venda reg = new Venda(prod, forma);
            try {
                reg.salvar();
            } catch (Exception ex) {
                ex.printStackTrace();
                JOptionPane.showMessageDialog(this, 
                    ex.getMessage());
                return;
            }
            
            JOptionPane.showMessageDialog(this, 
                    "Venda confirmada! Retire seu produto.");
        }
        else {
            JOptionPane.showMessageDialog(this, 
                    "Você deve informar uma Forma de Pagamento!");
        }
    }//GEN-LAST:event_btnComprarActionPerformed

