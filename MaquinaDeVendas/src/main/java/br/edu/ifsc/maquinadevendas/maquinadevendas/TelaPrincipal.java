    public void carregaProdutos() {
        try {
            produtos = Produto.buscaProdutos();
            
            if (produtos.size() > 0) {
                ImageIcon img1 = new ImageIcon(produtos.get(0).getImagem());
                lblImg1.setIcon(img1);
                lblCod1.setText(String.valueOf(produtos.get(0).getId()));
                lblDesc1.setText(produtos.get(0).getDescr());
                lblPreco1.setText("R$" + produtos.get(0).getPreco());
            } else if (produtos.size() > 1) {
                ImageIcon img2 = new ImageIcon(produtos.get(1).getImagem());
                lblImg2.setIcon(img2);
                lblCod2.setText(String.valueOf(produtos.get(1).getId()));
                lblDesc2.setText(produtos.get(1).getDescr());
                lblPreco2.setText("R$" + produtos.get(1).getPreco());
            } else if (produtos.size() > 2) {
                ImageIcon img3 = new ImageIcon(produtos.get(2).getImagem());
                lblImg3.setIcon(img3);
                lblCod3.setText(String.valueOf(produtos.get(2).getId()));
                lblDesc3.setText(produtos.get(2).getDescr());
                lblPreco3.setText("R$" + produtos.get(2).getPreco());
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
            System.exit(-1);
        }
    }

