    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        String descricao = tfDescricao.getText();
        String preco = tfPreco.getText();
        String imagem = tfImagem.getText();
        
        if (descricao.length() == 0) {
            JOptionPane.showMessageDialog(this, "Você deve informar a descrição");
            return;
        }
        if (preco.length() == 0) {
            JOptionPane.showMessageDialog(this, "Você deve informar o preço");
            return;
        }
        if (imagem.equals("Selecione...")) {
            JOptionPane.showMessageDialog(this, "Você deve selecionar uma imagem");
            return;
        }
        
        Produto produto = new Produto();
        produto.setDescr(descricao);
        produto.setPreco(Double.parseDouble(preco));
        produto.setImagem(imagem.replace("\\", "\\\\"));
        
        try {
            if (atualizando) {
                produto.atualizar();
            } else {
                produto.adicionar();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
            return;
        }
        
        atualizando = true;
        carregaProdutos();
        JOptionPane.showMessageDialog(this, "Produto salvo com sucesso!");
    }//GEN-LAST:event_btnSalvarActionPerformed

