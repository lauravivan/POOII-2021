    public void carregaProdutos() {
        try {
            produtos = Produto.buscaProdutos();
            preencheCampos();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro ao carregar produtos!");
            this.dispose();
        }
    }
    
    public void preencheCampos() {
        tfCodigo.setEnabled(false);
        
        if (produtos.size() > 0) {
            tfCodigo.setText(String.valueOf(produtos.get(produtoAtual).getId()));
            tfDescricao.setText(produtos.get(produtoAtual).getDescr());
            tfPreco.setText(String.valueOf(produtos.get(produtoAtual).getPreco()));
            tfImagem.setText(produtos.get(produtoAtual).getImagem());
            
            if (produtoAtual == 0) {
                btnAnterior.setEnabled(false);
            } else {
                btnAnterior.setEnabled(true);
            }
            
            if (produtoAtual == produtos.size() - 1) {
                btnSeguinte.setEnabled(false);
            } else {
                btnSeguinte.setEnabled(true);
            }
        } else {
            tfCodigo.setText("Automático...");
            btnAnterior.setEnabled(false);
            btnSeguinte.setEnabled(false);
            btnAdicionar.setEnabled(false);
            atualizando = false;
        }
    }
    
    public void apertaBotaoAdicionar() {
        btnAdicionar.doClick();
    }

